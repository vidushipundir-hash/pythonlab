# -------------------------------------------------
# Program Name : Intersection of Two Sets
# Description  : This program finds common
#                elements between two sets.
# -------------------------------------------------

# Creating two sets
setA = {"Apple", "Banana", "Mango"}
setB = {"Mango", "Orange", "Banana"}

# Finding intersection
common = setA.intersection(setB)

# Displaying result
print("Common Elements :", common)