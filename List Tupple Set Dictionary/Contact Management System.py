# Program 8: Contact Management

# List of names
names = ["Ravi", "Simran", "Ravi"]

# Tuple of cities
cities = ("Delhi", "Mumbai", "Chennai")

# Dictionary for contacts
contacts = {
    "Ravi": "9876543210",
    "Simran": "9123456780"
}

# Set for unique names
unique_names = set(names)

print("Cities:", cities)
print("Unique Names:", unique_names)
