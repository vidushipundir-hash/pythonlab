# Program 8: Simple Calculator

num1 = 10
num2 = 5
operator = '+'

if operator == '+':
    print("Result =", num1 + num2)
elif operator == '-':
    print("Result =", num1 - num2)
elif operator == '*':
    print("Result =", num1 * num2)
elif operator == '/':
    print("Result =", num1 / num2)
else:
    print("Invalid Operator")